package com.example.android3lesson6;

import androidx.annotation.Nullable;

public class Math {

    public int add(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int div(int a, int b) {
        return a / b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

}
